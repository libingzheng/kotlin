package com.bw.kotlin
/*
* 打印一个菱形
* */
fun main(args:Array<String>){
    //通过下面的方法可以在上面写调用对应的方法进行打印数据
    print1star()
    print3star()
    print5star()
    print7star()
    print5star()
    print3star()
    print1star()
}
fun print1star(){
    //替换上面的代码
    println("   *");
}
fun print3star(){
    //替换上面的代码
    println("  ***");
}
fun print5star(){
    //替换上面的代码
    println(" *****");
}
fun print7star(){
    //替换上面的代码
    println("*******");
}